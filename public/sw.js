// Service worker do Z Meta — hoje só existe pra suportar Web Push (Fase 5 do roadmap:
// notificações push). Não faz cache/offline (o PWA continua "instalável" via manifest.json
// só, sem estratégia de cache — escopo deliberadamente mínimo, evitar dor de cache velho
// servindo tela desatualizada num app que muda todo dia).

self.addEventListener("install", () => {
  self.skipWaiting();
});

self.addEventListener("activate", (event) => {
  event.waitUntil(self.clients.claim());
});

self.addEventListener("push", (event) => {
  let data = {};
  try {
    data = event.data ? event.data.json() : {};
  } catch (e) {
    data = { title: "Z Meta", body: event.data ? event.data.text() : "" };
  }
  const title = data.title || "Z Meta";
  const options = {
    body: data.body || "",
    icon: "/icon-192.png",
    badge: "/icon-192.png",
    data: { url: data.url || "/" },
  };
  event.waitUntil(self.registration.showNotification(title, options));
});

self.addEventListener("notificationclick", (event) => {
  event.notification.close();
  const url = (event.notification.data && event.notification.data.url) || "/";
  event.waitUntil(
    self.clients.matchAll({ type: "window", includeUncontrolled: true }).then((windowClients) => {
      for (const client of windowClients) {
        if (client.url.includes(url) && "focus" in client) return client.focus();
      }
      if (self.clients.openWindow) return self.clients.openWindow(url);
    })
  );
});
